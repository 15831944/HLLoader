// WADLump.cpp: implementation of the CWADLump class.
//
//////////////////////////////////////////////////////////////////////

#include "WADLump.h"

//////////////////////////////////////////////////////////////////////
// Construction/Destruction
//////////////////////////////////////////////////////////////////////

CWADLump::CWADLump()
{
	this->LumpData = 0;
	this->LumpLength = 0;
}

CWADLump::~CWADLump()
{
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
bool CWADLump::InitData(char* data, int len, char* name)
{
	this->LumpData = data;
	this->LumpLength = len;

	this->miptex.InitData(name, data, len);
	return true;
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
CWADMiptex* CWADLump::GetMiptex()
{
	return &this->miptex;
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
char* CWADLump::GetName()
{
	return miptex.GetName();
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
bool CWADLump::WriteBMP(char* path)
{
	return miptex.WriteBMP(path);
}
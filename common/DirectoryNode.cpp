#include "DirectoryNode.h"

//////////////////////////////////////////////////////////////////////
// Construction/Destruction
//////////////////////////////////////////////////////////////////////

CDirectoryNode::CDirectoryNode(char* name, int id, CDirectoryNode* parent)
{
	this->Name = name;
	this->ID = id;
	this->Parent = parent;
	this->NextSibling = 0;
}

CDirectoryNode::~CDirectoryNode()
{
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
void CDirectoryNode::SetParentNode(CDirectoryNode* parent)
{
	this->Parent = parent;
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
void CDirectoryNode::SetNextSibling(CDirectoryNode* nextSibling)
{
	this->NextSibling = nextSibling;
}
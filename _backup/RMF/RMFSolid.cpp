// RMFSolid.cpp: implementation of the CRMFSolid class.
//
//////////////////////////////////////////////////////////////////////

#include "RMFSolid.h"

#include <windows.h>
#include <gl/gl.h>
#include <gl/glu.h>

//////////////////////////////////////////////////////////////////////
// Construction/Destruction
//////////////////////////////////////////////////////////////////////

CRMFSolid::CRMFSolid()
{
}

CRMFSolid::~CRMFSolid()
{
	Clear();
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
void CRMFSolid::Clear()
{
	for (int i = 0; i < (int)this->SolidFaces.Length(); i++)
	{
		CRMFFace* face= this->SolidFaces[i];

		delete face;
	}
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
CRMFObject::ObjectType CRMFSolid::GetType()
{
	return CRMFObject::Solid;
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
void CRMFSolid::DrawGl()
{
	float color[4] = {0};
	this->SolidColor.Get(color);
	glLineWidth(2.0f);

	for (int i = 0; i < (int)this->SolidFaces.Length(); i++)
	{
		glColor3fv(color);
		this->SolidFaces[i]->DrawGl();
	}
}

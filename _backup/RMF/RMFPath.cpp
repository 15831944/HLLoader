#include "RMFPath.h"

//////////////////////////////////////////////////////////////////////
// Construction/Destruction
//////////////////////////////////////////////////////////////////////

CRMFPath::CRMFPath()
{
}

CRMFPath::~CRMFPath()
{
	Clear();
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
void CRMFPath::Clear()
{
	for (int i = 0; i < (int)this->PathCorners.Length(); i++)
	{
		CRMFCorner* corner = this->PathCorners[i];

		delete corner;
	}
	this->PathCorners.Resize(0);
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
CRMFObject::ObjectType CRMFPath::GetType()
{
	return CRMFObject::Path;
}

